public class Student {

    String firstName;
    String lastName;
    String studentNumber;
    int number_Of_Lessons_Comleted;

    public Student(String firstName, String lastName, String studentNumber, int number_Of_Lessons_Comleted) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.studentNumber = studentNumber;
        this.number_Of_Lessons_Comleted = number_Of_Lessons_Comleted;
    }
}
